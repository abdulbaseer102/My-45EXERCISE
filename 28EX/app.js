var age = 65;
function AgedStage() {
    for (var i = 0; i == 0; i++) {
        if (age < 2) {
            console.log("Person is Baby");
        }
        else if (age < 4) {
            console.log("Person Is Toddler");
        }
        else if (age < 13) {
            console.log("Person is Kid");
        }
        else if (age < 65) {
            console.log("The Person is Adult");
        }
        else {
            console.log("The Person is Elder");
        }
    }
}
AgedStage();
